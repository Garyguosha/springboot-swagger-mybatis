package com.lamd.provider.modules.thermallmager;

public class RGBModel {

    //像素点RGB值
    private  int R;
    private  int G;
    private  int B;

    public int getR() {
        return R;
    }

    public void setR(int r) {
        R = r;
    }

    public int getG() {
        return G;
    }

    public void setG(int g) {
        G = g;
    }

    public int getB() {
        return B;
    }

    public void setB(int b) {
        B = b;
    }

}
